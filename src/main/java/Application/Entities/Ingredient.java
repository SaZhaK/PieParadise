package Application.Entities;

public class Ingredient {

    private String id;
    private String name;
    private Type type;
    private String price;

    public static enum Type{MEAT, OTHER, SWEET, DAIRY, NUTRITIOUS}

    public Ingredient(String id, String name, Type type, String price) {
        this.id = id;
        this.name = name;
        this.type = type;
        this.price = price;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Type getType() {
        return type;
    }

    public void setType(Type type) {
        this.type = type;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }
}
